/** Resolve a key against a dictionary, substituting {var} placeholders */
export function resolve(dict: Dict, key: string, vars?: Record<string, string>): string {
  let str = dict[key] ?? key;
  if (vars) Object.entries(vars).forEach(([k, v]) => { str = str.replace(`{${k}}`, v); });
  return str;
}
